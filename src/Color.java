import java.io.*;
import java.util.*;

import static java.lang.Math.*;

public class Color {
    private int Red = 0;
    private int Green = 0;
    private int Blue = 0;

    public int getRed() {
        return Red;
    }

    public void setRed(int red) {
        Red = red;
    }

    public int getGreen() {
        return Green;
    }

    public void setGreen(int green) {
        Green = green;
    }

    public int getBlue() {
        return Blue;
    }

    public void setBlue(int blue) {
        Blue = blue;
    }


}